<script setup name="CountToDemo">
import CountTo from '../../components/CountTo/index.vue'

const data = reactive({
  setStartVal: 0,
  setEndVal: 2017,
  setDuration: 4000,
  setDecimals: 0,
  setSeparator: ',',
  setSuffix: ' rmb',
  setPrefix: 'Â¥ '
})

const {
  setStartVal,
  setEndVal,
  setDuration,
  setDecimals,
  setSeparator,
  setSuffix,
  setPrefix
} = toRefs(data)

const example = ref(null)

const start = () => {
  example.value.start()
}

const pauseResume = () => {
  example.value.pauseResume()
}

const _startVal = computed(() => {
  if (data.setStartVal) {
    return data.setStartVal
  } else {
    return 0
  }
})

const _endVal = computed(() => {
  if (data.setEndVal) {
    return data.setEndVal
  } else {
    return 0
  }
})
const _duration = computed(() => {
  if (data.setDuration) {
    return data.setDuration
  } else {
    return 100
  }
})
const _decimals = computed(() => {
  if (data.setDecimals) {
    if (data.setDecimals < 0 || data.setDecimals > 20) {
      alert('digits argument must be between 0 and 20')
      return 0
    }
    return data.setDecimals
  } else {
    return 0
  }
})

const _separator = computed(() => {
  return data.setSeparator
})

const _suffix = computed(() => {
  return data.setSuffix
})

const _prefix = computed(() => {
  return data.setPrefix
})
</script>

